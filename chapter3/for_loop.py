# string
for letter in "Hello World":
    print(letter.upper())

# list
my_list = [1, 2, 3, 4, 5]

for num in my_list:
    print(num)

# tuple list
for a, b in [(1, 2), (3, 4), (5, 6)]:
    print(a, b)

# dictionary
my_dict = {"name": "Alice", "age": 30}

for key, value in my_dict.items():
    print(f"{key}: {value}")

# set
my_set = {1, 2, 3, 4, 5}

for num in my_set:
    print(num)
