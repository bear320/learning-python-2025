# absolute value
print(abs(-10))  # 10
print(abs(10))  # 10

# string
print(str(10))  # '10'

# power
print(pow(10, 4))  # 10000

# maximum
print(max(10, 20, 30, 40, 50))  # 50

# minimum
print(min(10, 20, 30, 40, 50))  # 10

# round: for numbers ending in .5, Python rounds to the nearest even number
print(round(2.5))  # 2
print(round(3.5))  # 4

# integer
print(int(3.0))  # 3

# float
print(float(3))  # 3.0
