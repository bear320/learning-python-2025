name = "Oliver"
print(type(name))  # Output: <class 'str'>

age = 27
print(type(age))  # Output: <class 'int'>

height = 5.9
print(type(height))  # Output: <class 'float'>

is_married = False
print(type(is_married))  # Output: <class 'bool'>

family = ["Alice", "Bob", "Charlie"]
print(type(family))  # Output: <class 'list'>
